public class Unary_Operator {
    public static void main(String[] args) {
        int a = 10;
        int d = 10;
        int b = ++a;   // Increment Operator
        int c = d--;   // Decrement Operator
        System.out.println(a);
        System.out.println(b);
        System.out.println(d);
        System.out.println(c);
    }
}
