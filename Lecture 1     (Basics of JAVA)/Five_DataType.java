public class Five_DataType {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean t = true;
        System.out.println(t);
        float f = 10.58f;    // we use f to indicate that it is a floating point number because default it is double number
        System.out.println(f);
        int i = 80;
        System.out.println(i);

        // For extra space in memory
        // long
        // double
        // short
    }
}
